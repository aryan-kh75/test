#polymorphism is the ability of an object to take on many forms. It allows us to define methods in the child class that have the same name as the methods in the parent class. The child class can provide its own implementation of the method, which will be called when the method is invoked on an instance of the child class.
#In Python, we can achieve polymorphism through method overriding and operator overloading.
x="hello world"
y=['12','hi',-1,9.999]
z=('99.99','hello')
print(len(y))
print(len(x))
print(len(z))
